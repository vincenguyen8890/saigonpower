export const getFeaturedPlans = (count = 3): ElectricityPlan[] => {
  const featured = mockPlans.filter(p => p.badges.length > 0 && p.planType === 'residential')
  return featured.slice(0, count)
}

export const getPlansByType = (type: 'residential' | 'commercial'): ElectricityPlan[] => {
  return mockPlans.filter(p => p.planType === type || p.planType === 'both')
}
